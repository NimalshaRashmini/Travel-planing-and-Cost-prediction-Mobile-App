import 'package:flutter/material.dart';
import 'package:firebase_core/firebase_core.dart'; // Import Firebase Core
import 'screens/menu_screen.dart'; // Import MenuScreen
import 'screens/splash_screen.dart'; // Import SplashScreen
import 'screens/auth_screen.dart'; // Import AuthScreen
import 'firebase_options.dart'; // Import Firebase options (generated by FlutterFire CLI)

void main() async {
  // Ensure widgets are bound before initialization
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Firebase with platform-specific options
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  // Start the Flutter app
  runApp(ExploreSAE());
}

class ExploreSAE extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'ExploreSAE',
      theme: ThemeData(
        primarySwatch: Colors.blue,
        visualDensity: VisualDensity.adaptivePlatformDensity,
      ),
      debugShowCheckedModeBanner: false, // Remove the debug banner
      initialRoute: '/', // Set initial route
      routes: {
        '/': (context) => SplashScreen(), // Splash screen as the initial route
        '/menu': (context) => MenuScreen(), // Route for MenuScreen
        '/AuthScreen': (context) => AuthScreen(), // Route for AuthScreen
      },
    );
  }
}
